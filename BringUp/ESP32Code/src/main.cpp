void set_inputs(uint8_t ins) {
  for(uint8_t i = 0; i < 8; i++) {
    if((ins & 128) != 0) digitalWrite(PIN_OUT_DATA, HIGH);
    else digitalWrite(PIN_OUT_DATA, LOW);
    digitalWrite(PIN_OUT_CLOCK, HIGH);
    digitalWrite(PIN_OUT_CLOCK, LOW);
    ins <<= 1;
  }
  digitalWrite(PIN_OUT_DATA, LOW);
}

uint8_t get_outputs() {
  uint8_t res = 0;
  for(uint8_t i = 0; i < 8; i++) {
    res <<= 1;
    if(i == 0) {
      digitalWrite(PIN_PAR_LOAD, LOW);
      digitalWrite(PIN_PAR_LOAD, HIGH);
    }else {
      digitalWrite(PIN_IN_CLOCK, HIGH);
      digitalWrite(PIN_IN_CLOCK, LOW);
    }
    res |= digitalRead(PIN_IN_DATA) & 1;
  }
  return res;
}

void cpu_clock() {
  digitalWrite(PIN_CLK, HIGH);
  delayMicroseconds(CLOCK_TIME_US);
  digitalWrite(PIN_CLK, LOW);
  delayMicroseconds(CLOCK_TIME_US);
}

void reset() {
  cycle = 0;
  PC = 0;
  flag_I = 0;
  uart_state = 0;
  memset(RAM, 0, sizeof(RAM));
  digitalWrite(PIN_RST, HIGH);
  for(int i = 0; i < 5; i++) {
    digitalWrite(PIN_CLK, HIGH);
    delayMicroseconds(CLOCK_TIME_US);
    digitalWrite(PIN_CLK, LOW);
    delayMicroseconds(CLOCK_TIME_US);
  }
  digitalWrite(PIN_RST, LOW);
}

