void update() {
  if(cycle == 0) {
    set_inputs((ROM[PC] & 15) << 2);
    delayMicroseconds(CLOCK_TIME_US);
    cpu_clock();
    uint8_t immediate = ((ROM[PC] >> 4) & 15) << 2;
    /*if((ROM[PC] & 15) == 0 && !flag_I) {
      printf("LD addr %02x val %x\n", MAR, RAM[MAR]);
    }*/
    set_inputs(flag_I ? immediate : ((RAM[MAR] & 15) << 2));
    cycle = 1;
  }else if(cycle == 1) {
    cpu_clock();
    DB0 = get_outputs();
    cycle = 2;
  }else if(cycle == 2) {
    cpu_clock();
    cycle = 3;
  }else {
    cpu_clock();
    DB1 = get_outputs();
    PC++;
    cycle = 0;
    flag_I = DB1 >> 7;
    if(((DB1 >> 6) & 1) != 0) {
      PC = DB0 | ((DB1 & 15) << 8);
    }
    if(((DB1 >> 4) & 1) != 0) {
      MAR = DB0;
    }
    if(((DB1 >> 5) & 1) != 0) {
      RAM[MAR] = DB1 & 15;
      if(MAR == 0xEF) {
        if(!uart_state) uart_buff = DB1 & 15;
        else {
          uint8_t c = uart_buff | ((DB1 & 15) << 4);
          if(c == 0x0A) printf("\r\n");
          else if(c != 255) printf("%c", c);
          fflush(stdout);
        }
        uart_state = !uart_state;
      }
    }
  }
}

bool state = false;
void loop() {
  delay(2);
  digitalWrite(PIN_LED, state);
  state = !state;
  update();
  uint8_t outs = get_outputs();
  //printf("PC: %04x, Flags: %x\r\n", PC, outs >> 4);
}